function app = RKM4(h, a, nm) 
t=5/h;
N(1)=nm;
y(1)=((N(1)^2)*a)^(1/(1-a));
for i=1:t
   z1=(((1/N(i))-(a*N(i)*(y(i)^(a-1))))/(y(i)^a));
   z2=(((1/(N(i)+((h/2)*z1)))-(a*(N(i)+((h/2)*z1))*((y(i)+(h/2))^(a-1))))/((y(i)+(h/2))^a));
   z3=(((1/(N(i)+((h/2)*z2)))-(a*(N(i)+((h/2)*z2))*((y(i)+(h/2))^(a-1))))/((y(i)+(h/2))^a));
   z4=(((1/(N(i)+((h*z3))))-(a*(N(i)+(h*z3))*((y(i)+h)^(a-1))))/((y(i)+h)^a));
   N(i+1)=N(i)+((h/6)*(z1+(2*z2)+(2*z3)+z4));
   y(i+1)=y(i)+h; 
end
m=[1 (0.1/h)+1 (0.2/h)+1 (0.4/h)+1 (1/h)+1 (2/h)+1 (4/h)+1];
app=[N(m(1)) N(m(2)) N(m(3)) N(m(4)) N(m(5)) N(m(6)) N(m(7))];